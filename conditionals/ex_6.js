let weather = 'hail'
switch (weather) {
  case 'sunny':
    console.log("It's a sunny day!");
    break
  case 'rainy': 
    console.log("Grab your umbrella");
    break
  case 'cloudy':
    console.log("No need for sunscreen!");
    break
  case 'snowy':
    console.log("Brrrr");
    break
  default:
    console.log("Let's stay inside.");
}